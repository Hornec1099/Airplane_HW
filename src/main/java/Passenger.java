public class Passenger {

    private String name;
    private int NoOfBags;

    public Passenger(String name, int NoOfBags){
        this.name = name;
        this.NoOfBags =NoOfBags;
    }

    public String getName() {
        return name;
    }

    public int getNoOfBags() {
        return NoOfBags;
    }

}
